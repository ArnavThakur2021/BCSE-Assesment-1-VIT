"""A-11: Write a program to find the average marks scored by a student in 3
subjects"""

a=int(input('Write the score in 1st subject'))
b=int(input('Write the score in 2nd subject'))
c=int(input('Write the score in 3rd subject'))
avg=(a+b+c)/3
print('The average score of the subject is:- ',avg,'marks')
